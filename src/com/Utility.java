package com;

public class Utility {

    public static void printIntArray(int[] arr){

        for (int i : arr) {
            System.out.print(i + ", ");
        }
        System.out.println();
    }

    public static void printIntegerArray(Integer[] arr){

        for (int i : arr) {
            System.out.print(i + ", ");
        }
        System.out.println();
    }
}
